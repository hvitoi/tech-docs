class Main {

  public static void main(String[] args) {
    var color = "green";
    switch (color) {
      case "red":
        System.out.println("It's red!");
        break;
      case "green":
        System.out.println("It's green!");
        break;
      case "blue":
        System.out.println("It's blue!");
        break;
      default:
        System.out.println("It's something else!");
    }
  }

}

// + (addition)
// -- (subtraction)
// * (multiplication)
// / (division)
// % (modulus)
// ++ (increment)
// -- (decrement)

int main() {
  int a = 1 + 1;
  int b = 2 - 1;
  int c = 2 * 2;
  int d = 4 / 2;
  float e = 5 / (float)2;

  int f = 5 % 2;

  a++;
  a--;

  a += 1;
  a -= 1;
  a *= 1;
  a /= 1;
  a %= 1;

  return 0;
}
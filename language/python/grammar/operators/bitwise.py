# %%
a = 6  # 0000 0110
b = 12  # 0000 1100

# %%
# AND
a & b  # 0000 0100 (4)

# %%
# OR
a | b  # 0000 1110 (14)

# %%
# XOR
a ^ b  # 0000 1010 (10)

# %%
# NOT
~a  # -0000 0111 (-7)

# %%
# SHIFT LEFT
a << 1  # 0000 1100 (12)

# %%
# SHIFT RIGHT
a >> 1  # 0000 0011 (3)

# %%
# Summing two numbers
(a ^ b) + ((a & b) << 1)
